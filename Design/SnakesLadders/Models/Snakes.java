package SnakesLadders.Models;

public class Snakes {

    public Position start;
    public Position end;

    public void setStart(Position start) {
        this.start = start;
    }

    public void setEnd(Position end) {
        this.end = end;
    }

    public Position getEnd() {
        return end;
    }

    public Position getStart() {
        return start;
    }
}
